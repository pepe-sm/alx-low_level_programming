#include"3-calc.h"

/**
 * op_add - sum
 * @a: first int
 * @b: 2nd int
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: first int
 * @b: 2nd int
 *
 * Return: subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first int
 * @b: 2nd int
 *
 * Return: multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide
 * @a: first int
 * @b: 2nd int
 *
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder
 * @a: first int
 * @b: 2nd int
 *
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
